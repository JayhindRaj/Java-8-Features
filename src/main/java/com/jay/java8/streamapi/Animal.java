package com.jay.java8.streamapi;

public class Animal {
private String color;
private String name;

public Animal(String name, String color) {
	this.name = name;
	this.color = color;
}

public String getColor() {
	return color;
}

public void setColor(String color) {
	this.color = color;
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}


}
